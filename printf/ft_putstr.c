#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	a;

	a = 0;
	if (!str)
		str = "(null)";
	while (str[a] != '\0')
	{
		ft_putchar(str[a]);
		a++;
	}
	return (a);
}
